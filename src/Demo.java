public class Demo {
    Integer Dosa;
    Integer Chappathi;
    Integer Idli;


    public Demo(Integer dosa, Integer chappathi, Integer idli) {
        Dosa = dosa;
        Chappathi = chappathi;
        Idli = idli;
    }

    public Integer getDosa() {
        return Dosa;
    }

    public void setDosa(Integer dosa) {
        Dosa = dosa;
    }

    public Integer getChappathi() {
        return Chappathi;
    }

    public void setChappathi(Integer chappathi) {
        Chappathi = chappathi;
    }

    public Integer getIdli() {
        return Idli;
    }

    public void setIdli(Integer idli) {
        Idli = idli;
    }
}
